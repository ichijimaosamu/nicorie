<h3 class="representative__title">代表　山本達也</h3>

<p class="representative__text">20年間、カメラマン兼グラフィックデザイナーとして、神戸の老舗印刷出版社勤務。<br>
カメラマンとしては、学校案内・広報関連などを中心に、神戸市を代表する広報誌の表紙を3年間、<br>
消防局の広報撮影5年間、神戸市議会議員の広報撮影なども務める。<br>
某区写真コンク—ルでは特別審査員を務め、写真教室の講師も担当。<br>
グラフィックデザインでは、常に新規獲得を中心に任され、チーフデザイナーとして務める。<br>
2011年、大阪のものづくり企業に転職し、デザイン雑貨ブランドを代表と二人でゼロから立ち上げる。<br>
6年間、商品企画開発をはじめ、商品プロデュース、広告販促物制作、製造・生産・販売管理など<br>
あらゆる業務を経てブランドの構築スキルを習得する。<br>
ドイツ「ambiente」、東京「interiorlifestyle」など、国内外の大規模展示会への出展約10回。<br>
また、TV番組「探偵！ナイトスクープ」から出演オファーを受けるなど、テレビ出演も経験。<br>
2017年、古巣の印刷出版社より復職を打診され、アートディレクターとして再就職。<br>
印刷からの脱却を掲げ、様々な特殊案件に挑み続ける。<br>
2021年、さらなる自分の可能性を追求するため独立。<br>
「ニコリエデザインプランニング」を立ち上げる。</p>

